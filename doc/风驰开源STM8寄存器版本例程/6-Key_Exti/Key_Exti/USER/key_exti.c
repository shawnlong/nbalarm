#include "key_exti.h"
/**************************************************************************
 * 函数名：Buttom_conf
 * 描述  ：按键配置函数
 * 输入  ：无
 *
 * 输出  ：无
 * 返回  ：无 
 * 调用  ：外部调用 
 *************************************************************************/
void Buttom_conf(void)
{
  PD_DDR&=0x1f;/*设置数据方向寄存器 1为输出，0为输入*/
  PD_CR1|=0xe0;/*设置上拉输入*/
  PD_CR2|=0xe0;/*设置中断*/
  EXTI_CR1 &= (uint8_t)(~MASK_EXTI_CR1_PDIS);
  EXTI_CR1 |= (uint8_t)((uint8_t)(0x02) << 6);/*设置为下降沿触发中断*/
}

